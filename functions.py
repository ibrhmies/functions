def ibo():
    for i in range(11):
        print("ibo")

#bu şekilde ibo diye bir fonksiyon tanımlarsak programı çalıştırdığımızda fonksiyon çalışmayacak 
#çünkü fonksiyonu tanımlamadık
ibo()
#bu şekilde fonksiyonun adını yazıp yanına parantez koyarak 
#fonksiyonu çağırmış oluyoruz ve ekrana ibo yazdırıyor
